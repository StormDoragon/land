const BY_CODE = new Map(COUNTRIES.map((c) => [c.code, c]));

export function findCountry(code?: string | null): Country | undefined {
  if (!code) return undefined;
  return BY_CODE.get(code.toUpperCase());
}

/** Resolve a country (or the global view) to a map focus target. */
export function focusForCountry(
  code?: string | null,
): { lat: number; lng: number; zoom: number } {
  const c = findCountry(code);
  if (!c) return GLOBAL_VIEW;
  return { lat: c.lat, lng: c.lng, zoom: c.z ?? DEFAULT_COUNTRY_ZOOM };
}
