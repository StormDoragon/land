// Full ISO-3166-1 country list with approximate geographic centroids, used to
// focus the world map on a chosen country. Flag emoji are derived from the
// 2-letter code at runtime (regional-indicator symbols) so the data stays lean.

export interface Country {
  /** ISO-3166-1 alpha-2 code, uppercase. */
  code: string;
  name: string;
  lat: number;
  lng: number;
  /** Suggested map zoom; large countries get a lower value. */
  z?: number;
}

/** Default focus zoom when a country doesn't specify its own. */
export const DEFAULT_COUNTRY_ZOOM = 5;

/** The "no country / whole world" view. */
export const GLOBAL = "GLOBAL" as const;
export const GLOBAL_VIEW = { lat: 20, lng: 0, zoom: 3 };

/** 🇫🇷 from "FR" — offset each ASCII letter into the regional-indicator block. */
export function flagEmoji(code: string): string {
  if (!/^[A-Za-z]{2}$/.test(code)) return "🌍";
  return code
    .toUpperCase()
    .replace(/./g, (c) => String.fromCodePoint(127397 + c.charCodeAt(0)));
}

